function getTableInfo(testInfo){
	//复制元素
	for (var i = 0; i < testInfo.length; i++) {
		$(".infoTable").append($(".demo:first").clone());
		$(".infoTable").find(".demo:last-child").attr("id",'single'+testInfo[i].studentInfo.edu001_ID);
		$("#single"+testInfo[i].studentInfo.edu001_ID).addClass("single");
		$("#single"+testInfo[i].studentInfo.edu001_ID).find('.xy').html(testInfo[i].studentInfo.szxbmc);
	}
}


function exportHtml(){
	var all=$(".single");

	for (var i = 0; i <all.length ; i++) {
		html2canvas(all[i],{
			onrendered:function(canvas){
				$("#ele").append(canvas);
				var mycanvas1=$("canvas:last-child")[0];
				//将转换后的img标签插入到html中
				var img = convertCanvasToImage(mycanvas1);
				$('#imgDiv').append(img);//imgDiv表示你要插入的容器id
			},
			width:900,
			height:900
		})
	}
	$("#ele,.infoTable").hide();
}

function convertCanvasToImage(canvas){
	//新Image对象,可以理解为DOM;
	var image = new Image();

	//canvas.toDataURL返回的是一串Base64编码的URL,当然,浏览器自己肯定支持
	//指定格式PNG
	image.src = canvas.toDataURL("image/png");
	return image;
}

